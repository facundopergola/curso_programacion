class CarritoDeComida():
	def __init(self, name, modelo, ruedas ,publicidad):
		self.name = name
		self.modelo = modelo
		self.ruedas = ruedas
		self.publicidad = publicidad
	
	def get_name(self):
		return self.name
	
	def get_modelo(self):
		return self.modelo
	
	def get_ruedas(self):
		return self.ruedas

	def get_publicidad(self):
		return self.publicidad